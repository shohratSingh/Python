#here we will discuss about datatypes, there are 2 types (i)single value datatype (ii) multi value datadtype

#single value datatype

#(i) integer
a = 1
b = -1
print(a,b)
print(type(a),type(b))
print(id(a),id(b))

#(ii) float
p = 10.1
q = -10.1
print(p,q)
print(type(p),type(q))
print(id(p),id(q))

#(iii) complex
s = 100+2
print(s)
print(10+20J)
print(type(s))
print(id(s))

#(iv) boolean
a =10
b = 20
print(a>b)
print(a<b)
print(True+False)
print(True*False)

